complete the below code using relational and arithmetic operator
 */

void main(List<String> args) {
  int x = 5;
  int y = 10;

  print("is x equal y:");
  print(x==y);

  print("is x bigger than y:");
  print(x>y);

  print("is x not equal y:");
  print(x!=y);

  print("is x less than or equal y:");
  print(x<=y);

  print("the value of 5 plus 2 is");
  print(5+2);

  print("the value of 3 multiply 6 is");
  print(3*6);

  print("the value of 5 divide by 2 is");
  print(5/2);

  print("the integer value of 5 divide by 2 is");
  print(5~/2);

  print("is x between 10 and 30");
  print(x>10 && x<30);
}